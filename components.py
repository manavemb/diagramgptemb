def generate_import_statements(selected_providers):
    import_statements = set()
    for provider in selected_providers:
        for category in CLOUD_PROVIDERS[provider]:
            import_statements.add(f"from diagrams.{provider.lower().replace(' ', '')} import {category}")
    return "\n".join(sorted(import_statements))

def get_available_components(selected_providers):
    components = []
    for provider in selected_providers:
        for category in CLOUD_PROVIDERS[provider]:
            components.extend(CLOUD_PROVIDERS[provider][category].keys())
    return components

def prepare_llm_context(selected_providers):
    context = []
    for provider in selected_providers:
        for category, subcategories in CLOUD_PROVIDERS[provider].items():
            for component, import_path in subcategories.items():
                context.append(f"{component}: {import_path}")
    return "\n".join(context)
